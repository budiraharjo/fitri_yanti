<div class="page-title">Sejarah SMK Manba'el Huda</div>

	<div class='post-container clearfix'>
	<p style="line-height: 2;"> 
	SMK MANBAEL – HUDA  berdiri Pada Tanggal 01 Juli 2012 yang didasari dari keprihatinan akan banyaknya siswa dan siswi lulusan SMP dan MTs Sederajat yang tidak melanjutkan yang mana banyak dari para siswa dan siswi yang di latar belakangi oleh faktor Ekonomi dan Geografis. Maka dari itu, Terbentuknya SMK MANBA’EL – HUDA  ini adalah perwujudan dari Realisasi Program Yayasan Pendidikan Islam Manba’El – Huda.
	</p>
	</br>
	<p style="line-height: 2;"> 
	SMK MANBA'EL -  HUDA  ialah lembaga pendidikan yang berfokus pada peningkatan mutu pembelajaran serta skil untuk bersaing pada dunia kerja. selain dari peningkatan mutu lulusan, SMK MANBA'EL HUDA juga menitik beratkan pada pendidikan agama serta moral yang dapat membentengi  generasi muda hususnya lulusan kami dari pengarh dan hal - hal yang negatif.
	</p>
	</br>
	<p style="line-height: 2;"> 
	SMK MANBA'EL - HUDA Memiliki Program Keahlian Agribisnis tanaman Pangan dan Hortikultura, dan dibekali pula dengan keahlian lain seperti Keterampilan Komputer dan Pemanfaatan limbah menjadi Barang ekonomi.
	</p>
	</br>
	<p style="line-height: 2;">
	Melihat masi banyaknya anak usia sekolah yang tidak melanjutkan terutama di daerah mandalawangi, dengan berbagai faktor di antaranya ialah
	</p>
	</br>
	<p style="line-height: 2;"> 
	</br>
1. Faktor Ekonomi</br>
    Faktor ekonomi penduduk di daerah mandalawangi 
    ialah menengah ke bawah dimana 80% Masyarakatnya 
    berpropesi sebagai buruh tani dan buruh serabutan 
    yang mana penghasilan perbulannya tidak tentu.</br></br>

2. Faktor Geografis</br>
    Faktor Geografis ini juga turut mempengaruhi 
    beberapa  daerah terdalam karena jauhnya lembaga  
    pendidikan.</br></br>

3. Faktor kurangnya Kepedulian Orang Tua Terhadap Pendidikan Aanaknya</br>
    Kepedulian orang tua terhadap pendidikan anaknya sangatlah minim dilihat dari banyaknya anak
    usia sekolah yang terkadang banyak ikut bekerja ke daerah perkotaan meski hanya mengantongi ijazah SMP bahkan SD, dimana kurangnya pemahaman orang tua akan pendidikan serta pemahaman akan kebutuhan SDM yang dibutuhkan oleh dunia kerja. 
	</p>
	</br></br>
	<p style="line-height: 2;"> 
	Akhirnya dari kesimpulan dan data serta latar belakang dari permasalahan tersebut, kepedulian lembaga SMK MANBAEL - HUDA yang dinaungi oleh YAYASAN PENDIDIKAN ISLAM MANBA'EL - HUDA, Mendirikan sekolah yang mana memiliki Jurusan / Program Keahlian Agribisnis Tanaman Pangan dan Hortikultura yang sesuai dengan daerah Mandalawani yang mana penghasilan utamanya dari bertani.
	</p>
	</br>
	<p style="line-height: 2;"> 
	 Pada SMK MAMNBAEL - HUDA biaya pendidikan hampir semua kami gratiskan karna mengingat banyaknya siswa yang latar belakang keluarganya ialah keluarga  berekonomi menengah kebawah. Selain itu kami juga sediakan fasilitas asrama bagi siswa siswi yang lokasi rumahnya jauh dari sekolah.
	</p>
